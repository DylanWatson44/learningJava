package binarySearch;

public class Student {
private int age;
private String name;

public Student(String Name, int Age){
	age=Age;
	name=Name;
}

public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
}
